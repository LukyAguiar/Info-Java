public class ExpressoesAritmeticas {

    public static void formatoAritmetico(){
        System.out.println("- Expressões Aritméticas'");
        System.out.println(" ( Expressão = 4 + 5 ) - (Resultado =) - (Valor Numérico = 9) ");
    }

    public static void exemplosAritmeticos(){
        System.out.println("- Exemplos de expressões aritméticas");
        System.out.println("2 * 6 / 3 - RESULTADO = 4 (Multiplicação sempre em primeiro)");
        System.out.println("3 + 2 * 4 - RESULTADO = 11");
        System.out.println("(3 + 2) * 4 - RESULTADO = 20 (Com o uso do parentese a prioridade muda)");
        System.out.println("60 / (3 +2 ) * 4 - RESULTADO = 48");
        System.out.println("60 * ((3+2) * 4) - RESULTADO = 3");
        System.out.println("-----------------------------------");
        System.out.println("- Exemplos com o operador 'mod' - Resto da divisão");
        System.out.println("14 % 3 - RESULTADO = 2");
        System.out.println("19 % 5 - RESULTADO = 4");
        System.out.println("-------------------------------");
    }

    public static void operadoresAritmeticos(){
        System.out.println("-------------------------------");
        System.out.println("Operadores | Significado      |");
        System.out.println("     +     | Adição           |");
        System.out.println("     -     | Subtração        |");
        System.out.println("     *     | Multiplicação    |");
        System.out.println("     /     | Divisão          |");
        System.out.println("     %     | Resto da divisão |");
    }
}
